#include <iostream>
using namespace std;

int main()
{
    int n = 0;

    if (n > 0)
    {
        cout << "n is positive";
    }

    else if (n < 0)
    {
        cout << "n is negative";
    }

    else
    {
        cout << "n is zero";
    }

    return 0;
} // same syntax as C